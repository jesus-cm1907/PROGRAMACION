    private boolean validateFields() {
        if (inputNombreEquipo.getText().toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el nombre", "Atenci贸n", JOptionPane.INFORMATION_MESSAGE);
            inputNombreEquipo.requestFocus();
            return false;
        }

        if (inputVictoriasEquipo.getText().toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar las victorias", "Atenci贸n", JOptionPane.INFORMATION_MESSAGE);
            inputVictoriasEquipo.requestFocus();
            return false;
        }

        if (inputDerrotasEquipo.getText().toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar las derrotas", "Atenci贸n", JOptionPane.INFORMATION_MESSAGE);
            inputDerrotasEquipo.requestFocus();
            return false;
        }
        return true;
    }
